public class conditional2
{
    public static void main(String args[])
    {

         int marks = 30;
         System.out.print(marks);
         switch (marks){
             case 60:

                 System.out.println("second class");

             break;

             case 70:
             System.out.println("firstclass");
             break;

             case 80:
              System.out.println("distinction");
              break;

             default:
                 System.out.println("nooo");


         }


    }

}
